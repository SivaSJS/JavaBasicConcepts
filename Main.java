package com.company;

public class Main {

    public static void main(String[] args) {
        // primitive data types

        byte a = 1; // can store a value from -128 to 128  byte -> 1
        short b = 20000; // can store a value from -32k to 32k  byte -> 2
        int c = 2_000_000; // -2b to 2b  byte -> 4
        long d = 3_000_000_000l; //can store value upto -6b to 6b  byte -> 8
        float f = 3.14f; // can store decimal values byte -> 4
        double e = 4.44444d; // can store double byte -> 8
        char character = '@'; // similarly 'a to z' byte -> 2
        boolean isTrue = true; // true or false byte -> 1
    }

}
